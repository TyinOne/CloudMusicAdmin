<template>
  <div class="layout-navbars-breadcrumb-user" :style="{ flex: layoutUserFlexNum }">
    <div class="layout-navbars-breadcrumb-user-icon" @click="onSearchClick">
      <el-icon :title="'菜单搜索'">
        <ele-Search />
      </el-icon>
    </div>
    <div class="layout-navbars-breadcrumb-user-icon" @click="onLayoutSettingClick">
      <i class="icon-skin iconfont" :title="'布局配置'"></i>
    </div>

    <div class="layout-navbars-breadcrumb-user-icon" @click="onReloadClick">
      <svg-icon name="ele-RefreshLeft" :title="'刷新'"></svg-icon>
    </div>
    <div class="layout-navbars-breadcrumb-user-icon">
      <el-popover placement="bottom" trigger="click" :width="300">
        <template #reference>
          <el-badge :is-dot="true">
            <el-icon :title="'消息'">
              <ele-Bell />
            </el-icon>
          </el-badge>
        </template>
        <UserNews />
      </el-popover>
    </div>
    <div class="layout-navbars-breadcrumb-user-icon mr10" @click="onScreenfullClick">
      <i
          class="iconfont"
          :title="state.isScreenfull ? '开全屏' : '关全屏'"
          :class="!state.isScreenfull ? 'icon-fullscreen' : 'icon-tuichuquanping'"
      ></i>
    </div>
    <el-dropdown :show-timeout="70" :hide-timeout="50" @command="onHandleCommandClick">
			<span class="layout-navbars-breadcrumb-user-link">
				<img :src="getUserInfos.avatar" class="layout-navbars-breadcrumb-user-link-photo mr5"  alt=""/>
				{{ getUserInfos.nickName === '' ? 'common' : getUserInfos.nickName }}
				<el-icon class="el-icon--right">
					<ele-ArrowDown />
				</el-icon>
			</span>
      <template #dropdown>
        <el-dropdown-menu>
          <el-dropdown-item command="/home">{{ '首页' }}</el-dropdown-item>
          <el-dropdown-item command="/gitee">{{ '代码仓库' }}</el-dropdown-item>
          <el-dropdown-item command="/personal">{{ '个人中心' }}</el-dropdown-item>
          <el-dropdown-item command="open">{{ '百度' }}</el-dropdown-item>
          <el-dropdown-item command="/404">{{ '404' }}</el-dropdown-item>
          <el-dropdown-item divided command="logOut">{{ '退出登录' }}</el-dropdown-item>
        </el-dropdown-menu>
      </template>
    </el-dropdown>
    <Search ref="searchRef" />
  </div>
</template>
<script lang="ts" setup>
import {computed, getCurrentInstance, reactive, ref} from "vue";
import {useRouter} from "vue-router";
import {useStore} from "@renderer/store";
import {Local, Session} from "@renderer/utils/storage";
import {ElMessage, ElMessageBox} from "element-plus";
import {resetRoute} from "@renderer/router";
import Search from '@renderer/layout/navBars/breadcrumb/search.vue'
import screenfull from 'screenfull';
import UserNews from '@renderer/layout/navBars/breadcrumb/userNews.vue'
import SvgIcon from "@renderer/components/svgIcon/index.vue";
const { ipcRenderer } = window;
const { proxy } = <any>getCurrentInstance();
const router = useRouter();
const store = useStore();
const searchRef = ref();
const state = reactive({
  isScreenfull: false,
  disabledI18n: 'zh-cn',
  disabledSize: 'large',
});

// 获取用户信息 vuex
const getUserInfos = computed(() => {
  return <any>store.state.userInfos.userInfos;
});
// 获取布局配置信息
const getThemeConfig = computed(() => {
  return store.state.themeConfig.themeConfig;
});
// 设置分割样式
const layoutUserFlexNum = computed(() => {
  let num: string | number = '';
  const { layout, isClassicSplitMenu } = getThemeConfig.value;
  const layoutArr: string[] = ['defaults', 'columns'];
  if (layoutArr.includes(layout) || (layout === 'classic' && !isClassicSplitMenu)) num = '1';
  else num = '';
  return num;
});
const onReloadClick = () => {
  window.location.reload()
}
// 布局配置 icon 点击时
const onLayoutSettingClick = () => {
  proxy.mittBus.emit('openSettingsDrawer');
};
// 下拉菜单点击时
const onHandleCommandClick = (path: string) => {
  if (path === 'logOut') {
    ElMessageBox({
      closeOnClickModal: false,
      closeOnPressEscape: false,
      title: '提示',
      message: '此操作将退出登录, 是否继续?',
      showCancelButton: true,
      confirmButtonText: '确定',
      cancelButtonText: '取消',
      buttonSize: 'default',
      beforeClose: (action, instance, done) => {
        if (action === 'confirm') {
          instance.confirmButtonLoading = true;
          instance.confirmButtonText = '退出中';
          setTimeout(() => {
            done();
            setTimeout(() => {
              instance.confirmButtonLoading = false;
            }, 300);
          }, 700);
        } else {
          done();
        }
      },
    })
        .then(async () => {
          Session.clear(); // 清除缓存/token等
          await resetRoute(); // 删除/重置路由
          ElMessage.success('安全退出成功！');
          setTimeout(() => {
            window.location.href = ''; // 去登录页
          }, 500);
        })
        .catch(() => {});
  } else if (path === 'open') {
    ipcRenderer.invoke('open-web','https://www.baidu.com');
  } else {
    router.push(path);
  }
};
// 菜单搜索点击
const onSearchClick = () => {
  searchRef.value.openSearch();
};
// 全屏点击时
const onScreenfullClick = () => {
  if (!screenfull.isEnabled) {
    ElMessage.warning('暂不不支持全屏');
    return false;
  }
  screenfull.toggle();
  screenfull.on('change', () => {
    state.isScreenfull = screenfull.isFullscreen;
  });
};
</script>
<script lang="ts">
export default {
  name: "layoutBreadcrumbUser"
}
</script>


